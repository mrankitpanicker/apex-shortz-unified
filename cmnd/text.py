def convert_number_to_hindi(match):
    """Converts a matched percentage number to its Hindi word equivalent."""
    num = int(match.group(1))
    return num_to_hindi.get(num, match.group(0))


def fix_hindi_pronunciation(txt: str) -> str:
    """Fixes common pronunciation issues for better TTS quality (e.g., 'प्रतिशत' -> 'प्रतिशथ')."""
    return txt.replace("प्रतिशत", "प्रतिशथ")


def format_srt_time(seconds: float) -> str:
    """Formats time in SRT format (HH:MM:SS,mmm)."""
    total_ms = int(round(seconds * 1000))
    h = total_ms // 3600000
    m = (total_ms % 3600000) // 60000
    s = (total_ms % 60000) // 1000
    ms = total_ms % 1000
    return f"{h:02}:{m:02}:{s:02},{ms:03}"


def format_ass_time(seconds: float) -> str:
    """Formats time in ASS format (H:MM:SS.cc)."""
    total_ms = int(seconds * 1000)
    h = total_ms // 3600000
    m = (total_ms % 3600000) // 60000
    s = (total_ms % 60000) // 1000
    cs = (total_ms % 1000) // 10
    return f"{h}:{m:02}:{s:02}.{cs:02}"


def get_wav_duration(wav_file: Path) -> float:
    """Calculates the duration of a WAV file in seconds."""
    try:
        with wave.open(str(wav_file), "rb") as wf:
            frames = wf.getnframes()
            rate = wf.getframerate()
            return frames / float(rate)
    except Exception:
        # Fallback to ffprobe
        res = subprocess.run(
            [
                "ffprobe",
                "-i",
                str(wav_file),
                "-show_entries",
                "format=duration",
                "-v",
                "quiet",
                "-of",
                "csv=p=0",
            ],
            capture_output=True,
            text=True,
        )
        try:
            duration = float(res.stdout.strip())
            return duration
        except (ValueError, IndexError):
            print(
                f"Warning: Could not get duration for {wav_file} using ffprobe. The file might be corrupted or zero-length."
            )
            return 0.0


def clean_tts_output(text: str) -> str:
    """
    Cleans up common artifacts from XTTS Hindi generation/tokenization,
    specifically duplicated characters/matras at the end of words.
    Example: 'मेषष' -> 'मेष', 'राशिि' -> 'राशि', 'होगा।।' -> 'होगा।'
    """
    # Pattern: finds any Devanagari character/punctuation (U+0900 to U+097F) followed immediately by itself.
    # We replace the sequence with a single instance of the character.
    cleaned_text = re.sub(r"([\u0900-\u097F])\1+", r"\1", text)

    # Safety check for duplicated ASCII punctuation
    cleaned_text = re.sub(r"([.,;!?:])\1+", r"\1", cleaned_text)

    return cleaned_text


def clean_text_for_timing(text):
    """
    1. Removes duplicated characters (TTS output artifact).
    2. Removes punctuation, leaving Hindi, English, numbers, and space
    for accurate word counting/split timing.
    """
    # 1. Clean duplicated characters
    text = clean_tts_output(text)
    # 2. Remove punctuation
    return re.sub(r"[^ऀ-ॿ0-9A-Za-z\s]", "", text)


# Helper function to run FFmpeg with diagnostics
def run_ffmpeg_command(cmd, desc):
    print(f"\n⚙️ Running FFmpeg command for: {desc}")
    print(
        f"Command: {' '.join(shlex.quote(c) for c in cmd)}"
    )  # Use shlex.quote for safe printing
    try:
        result = subprocess.run(
            cmd,
            check=True,
            capture_output=True,
            text=True,
            encoding="utf-8",
            errors="replace",
        )
        print(f"✅ FFmpeg success: {desc}")
        # print("Output:", result.stdout) # Uncomment for verbose FFmpeg logging
        # print("Warnings/Info:", result.stderr)
        return True
    except subprocess.CalledProcessError as e:
        print(f"\n🚨 FFMPEG ERROR DETAILS for {desc} (Return Code {e.returncode}):")
        print("---------------------------------------------------------")
        print("STDOUT:", e.stdout)
        print("STDERR:", e.stderr)
        print("---------------------------------------------------------")
        raise RuntimeError(f"FFmpeg failed during {desc}. See details above.")
    except FileNotFoundError:
        print(
            "\n🚨 ERROR: FFmpeg executable not found. Make sure it is installed and added to your system PATH."
        )
        raise FileNotFoundError("FFmpeg executable not found.")


